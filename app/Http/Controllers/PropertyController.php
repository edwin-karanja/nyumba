    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PropertyRequest $r)
    {
        $property = new Property();
        $property->location = $r->input('location');
        $property->bedrooms = $r->input('bedrooms');
        $property->published = $r->input('published');

        $path = $r->file('image')->store('public');
        $property->image_path = $path;

        auth()->user()->properties()->save($property);

        return redirect()->to('/property');
    }

